    private void clearPanel()
    {
        jProgressBar1.setValue(0);
        labelLen.setText("0,00");
        AntColony.cmax = 0;
        if(Map.citiesCount != 0)
        {
            AntColony.n = 0;
            AntColony.c = 0;
            this.mPanel = new Map();
            this.forDrawPanel.removeAll();
            this.forDrawPanel.setLayout(new GridLayout());
            this.forDrawPanel.add(this.mPanel);
            this.repaint();
            this.jLabel8.setText("");
        }
        Map.citiesCount = 0;
    }
    
