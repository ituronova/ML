    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        AntColony.algorithm = jComboBox1.getSelectedIndex();
        jProgressBar1.setValue(0);
        labelLen.setText("0,00");
        AntColony.n = Map.citiesCount;
        AntColony.c = 0;
        System.out.print(AntColony.n);
        if(AntColony.n > 100)
        {
            this.jLabel8.setText("Error: Too many cities!");
        }
        else if(AntColony.m < 0)
        {
            this.jLabel8.setText("Error: Set parameters!");
        }
        else if(AntColony.n < 0)
        {
            this.jLabel8.setText("Error: Add cities!");
        }
        else if(AntColony.m > 100)
        {
            this.jLabel8.setText("Error: Too many ants!");
        }
        else
        {
            this.jLabel8.setText("");
            for(int i = 0; i < n; i++)
            {
                cities[i][0] = Map.cities.get(i).location.x; 
                cities[i][1] = Map.cities.get(i).location.y;
            }
            // main algorithm
            while(AntColony.c < AntColony.cmax) 
            {
                AntColony();
                AntColony.c++;
            }
            exec = Executors.newScheduledThreadPool(1);
            Runnable task = new Runnable() {
            int seconds = 0;
            @Override
            public void run() {
                labelLen.setText(String.format( "%.2f", AntColony.shortMax[seconds]));
                jProgressBar1.setValue((seconds+1)*100/(AntColony.cmax));
                mPanel.hightLight(seconds);
                seconds++;
                if (seconds > AntColony.cmax-1) {
                    jLabel8.setText("Finished!");
                    final Chart demo = new Chart("Final chart", "Algorithm: " + jComboBox1.getSelectedItem());
                    demo.pack();
                    demo.setVisible(true);
                    exec.shutdown();
                }
                }
            };
            exec.scheduleAtFixedRate(task, 1, jSlider1.getValue()/10, TimeUnit.SECONDS);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

